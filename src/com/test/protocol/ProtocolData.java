package com.test.protocol;

public class ProtocolData {
	private byte cmd;
	private byte type;
	private byte opt;


	public byte getCmd() {
		return cmd;
	}
	public void setCmd(byte cmd) {
		this.cmd = cmd;
	}
	public byte getType() {
		return type;
	}
	public void setType(byte type) {
		this.type = type;
	}
	public byte getOpt() {
		return opt;
	}
	public void setOpt(byte opt) {
		this.opt = opt;
	}


	
}
